#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t				i;
	const unsigned char	*us1;
	const unsigned char	*us2;

	us1 = (const unsigned char *) s1;
	us2 = (const unsigned char *) s2;
	i = 0;
	while (us1[i] && us1[i] == us2[i])
		i++;
	return (us1[i] - us2[i]);
}
